    from engine.economy import build_economic_context
    persona = ADVISOR_SYSTEMS.get(state.player_id, _DEFAULT_ADVISOR)
    system = persona + _ECONOMIC_ADVISOR_ADDON
    context = state.to_context_summary()
    econ = build_economic_context(state)
    full_context = f"{context}\n\n{econ}" if econ else context
    messages = [LLMMessage("user", f"{full_context}\n\n【質問】{player_question}")]
    return llm.chat(system, messages)


